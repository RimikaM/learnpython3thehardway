# addition and division returning a floating point
print ("Hens", 25.0 + 30.0 / 6.0)
# sub, multiply, mod (returns remainder), returning a floating point
print ("Roosters", 100.0 - 25.0 * 3.0 % 4.0)

print ("Now I will count the eggs: ")

# math resulting in floating point
print (3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0)

print ("Is it true that 3 + 2 < 5 - 7?")

# 5 is not less that -2, so it will return false
print (3.0 + 2.0 < 5.0 - 7.0)

# print 5.0
print ("What is 3 + 2?", 3.0 + 2.0)
# print -2.0
print ("What is 5 - 7?", 5.0 - 7.0)

print ("Oh, that's why it's False.")

print ("How about some more")

# 5 is greater than -2, so it will return true
print ("Is it greater?", 5 > -2)
# 5 is greater than or equal to -2, so it will return true
print ("Is it greater or equal?", 5  >= -2)
# 5 is not less than or equal to -2, so it will return false
print ("Is it less or equal?", 5 <= -2)
